    // Acao do botao add
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cilindro = Integer.parseInt(jTextField1.getText());
        if (cilindro >= 0) {
            int c = Integer.parseInt(jTextField1.getText());
            Escaloneitor.cilindros.add(c);
            jTextField1.setText("");
            jLabel5ActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(null, "O valor do cilindro deve ser maior ou igual a zero");
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    // Acao do botao Ok 
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Escaloneitor.CILINDRO_INICIAL = Integer.parseInt(jTextField2.getText());
        if (Escaloneitor.CILINDRO_INICIAL < 0) {
            JOptionPane.showMessageDialog(null, "O valor deve ser maior ou igual a zero");
            jTextField2.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Cilindro inicial definido com sucesso");
        }
        jTextField2.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jLabel5ActionPerformed(java.awt.event.ActionEvent evt) {
        String exbCilindros = "";
        for (int i = 0; i < Escaloneitor.cilindros.size(); i++) {
            exbCilindros = exbCilindros + Escaloneitor.cilindros.get(i) + " ";
        }
        jLabel5.setText(exbCilindros);
    }

